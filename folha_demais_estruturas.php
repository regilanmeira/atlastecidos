Parede celular: é uma estrutura importante na diferenciação das células animal e vegetal. Ausente em células animais, a parede celular está localizada externamente à membrana plasmática e possui como principal componente a celulose. 
Citoplasma: está localizado entre a membrana plasmática e o núcleo da célula. No citoplasma ocorrem diferentes reações químicas e muitas substâncias do metabolismo da planta são acumuladas, além disso, ele facilita a troca de substâncias dentro da célula e entre as células adjacentes. 
Membrana plasmática: está situada internamente à parede celular, envolvendo, portanto, o citoplasma. A membrana plasmática desempenha diferentes funções, merecendo destaque o controle da entrada e saída de substâncias da célula. 
