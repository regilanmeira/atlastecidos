                <p class="mb-4">
                                Todas as plantas apresentam vasos condutores de seiva, que se assemelham aos vasos sanguíneos presentes no corpo dos mamíferos, e são esses vasos os responsáveis pelo transporte de seiva no interior da planta. Eles podem ser encontrados nas raízes, caules e também nas folhas, formando o que chamamos de nervuras foliares. De acordo com a forma com que essas nervuras estão dispostas nas folhas podemos classificá-las em paralelinérveas e peninérveas. As folhas paralelinérveas possuem suas nervuras paralelas entre si, e são encontradas nas plantas monocotiledôneas (que possuem somente um cotilédone). Já as folhas peninérveas, encontradas na maioria das plantas, possuem suas nervuras ramificadas e podem ser encontradas nas plantas dicotiledôneas (que possuem dois cotilédones).

